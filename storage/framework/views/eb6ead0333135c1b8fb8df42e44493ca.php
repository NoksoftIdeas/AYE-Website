<section id="what-is-sickle-cell" class="section-padding bg-gray-50">
    <div class="container-custom">
        <div class="max-w-4xl mx-auto">
            <h2 class="text-primary mb-6">What is Sickle Cell Disease?</h2>
            <div class="prose prose-lg max-w-none text-gray-700">
                <p class="mb-4">
                    Sickle cell disease (SCD) is a group of inherited red blood cell disorders. People with SCD have abnormal hemoglobin, called hemoglobin S or sickle hemoglobin, in their red blood cells.
                </p>
                <p class="mb-4">
                    Hemoglobin is a protein in red blood cells that carries oxygen throughout the body. In SCD, the abnormal hemoglobin causes red blood cells to become hard, sticky, and shaped like a crescent or sickle. These sickle-shaped cells die early, causing a constant shortage of red blood cells (anemia).
                </p>
                <p class="mb-4">
                    When traveling through small blood vessels, sickle cells can get stuck and block blood flow. This can cause severe pain and serious complications such as infection, acute chest syndrome, and stroke.
                </p>
                <div class="bg-primary-50 border-l-4 border-primary p-6 rounded-r-lg mt-6">
                    <p class="font-semibold text-primary mb-2">Key Points:</p>
                    <ul class="list-disc list-inside space-y-2">
                        <li>SCD is a genetic disorder present from birth</li>
                        <li>It affects red blood cells and their ability to carry oxygen</li>
                        <li>Early diagnosis and proper management can significantly improve quality of life</li>
                        <li>It's most common in people of African, Mediterranean, and Middle Eastern descent</li>
                    </ul>
                </div>
            </div>
        </div>
    </div>
</section>

<section id="causes-genetics" class="section-padding">
    <div class="container-custom">
        <div class="max-w-4xl mx-auto">
            <h2 class="text-secondary mb-6">Causes & Genetics</h2>
            <div class="prose prose-lg max-w-none text-gray-700">
                <p class="mb-4">
                    Sickle cell disease is inherited, which means it is passed from parent to child through genes. A person inherits two genes for hemoglobin—one from each parent.
                </p>
                <h3 class="text-xl font-semibold mt-6 mb-3">Genetic Inheritance Patterns:</h3>
                <ul class="list-disc list-inside space-y-2 mb-6">
                    <li><strong>Sickle Cell Disease (HbSS):</strong> Inherits two sickle cell genes, one from each parent</li>
                    <li><strong>Sickle Cell Trait (HbAS):</strong> Inherits one sickle cell gene and one normal gene</li>
                    <li><strong>Other Variants:</strong> HbSC, HbS Beta Thalassemia, and other combinations</li>
                </ul>
                <div class="bg-secondary-50 border-l-4 border-secondary p-6 rounded-r-lg">
                    <p class="font-semibold text-secondary mb-2">Important to Know:</p>
                    <ul class="list-disc list-inside space-y-2">
                        <li>Sickle cell trait carriers usually don't have symptoms</li>
                        <li>Two carriers have a 25% chance with each pregnancy of having a child with SCD</li>
                        <li>Genetic counseling can help families understand their risk</li>
                    </ul>
                </div>
            </div>
        </div>
    </div>
</section>

